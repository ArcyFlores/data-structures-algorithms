# 🌟 Arrays in Python: Introduction and Basic Operations 🌟

# 1️⃣ Create an array (list in Python)
numbers = [10, 20, 30, 40, 50]  # 📦 Array of numbers

# 🖨️ Print the array
print("📋 Initial Array:", numbers)

# 2️⃣ Access an element using its index
# 🎯 Accessing the second element (index 1)
second_element = numbers[1]
print("🎯 Accessed Element (index 1):", second_element)

# 3️⃣ Update an element in the array
# ✏️ Update the second element (index 1) to a new value
numbers[1] = 25
print("✏️ Updated Array:", numbers)

# 4️⃣ Access multiple elements (optional, advanced for beginners)
# 🔍 Slicing to access the first three elements
first_three_elements = numbers[:3]
print("🔍 First Three Elements:", first_three_elements)